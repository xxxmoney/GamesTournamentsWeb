<script lang="ts" setup>
const { modelValue, label, showLabel, min, max } = defineProps({
  modelValue: {
    type: String,
    required: true
  },
  label: {
    type: String,
    default: ''
  },
  showLabel: {
    type: Boolean,
    default: true
  },
  min: {
    type: Number,
    default: null
  },
  max: {
    type: Number,
    default: null
  },
  step: {
    type: Number,
    default: 1
  }
})

const emit = defineEmits(['update:modelValue'])

const computedValue = useComputedWithEmit(modelValue, emit, 'modelValue')
</script>

<template>
  <CommonWithLabel :label="label" :showLabel="showLabel">
    <InputNumber
      v-model="computedValue"
      :max="max"
      :min="min"
      :placeholder="label as string"
      :step="step"
    />
  </CommonWithLabel>
</template>
